    static void cargarFacturas(){

        modelo.removeAllElements();
        try {
            ResultSet rs = null;
            Statement sentencia = BD.conexion.createStatement();
            String sql="Select * from factura";
            rs= sentencia.executeQuery(sql);
            
            while(rs.next()){
                
                modelo.addElement(new Factura( rs.getInt("cod_factura") , rs.getTimestamp("fecha") , rs.getInt("cod_empleado") ) );
                
            }
            
            
        } catch (SQLException ex) {
            Logger.getLogger(FacturasPDF.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }
    
    static void descargarPDF(int cod_factura){
       
        
        try {
            ResultSet rs = null;
            Statement sentencia = BD.conexion.createStatement();
            String sql="Select pdf from factura where cod_factura="+cod_factura;
            rs= sentencia.executeQuery(sql);
            
            int i=1;
            InputStream in = null;
            
            while(rs.next()){
                in = rs.getBinaryStream("pdf");
            }

            int available1 = in.available();
            byte[] bt = new byte[available1];
            in.read(bt);

            FileOutputStream fout = new FileOutputStream("Factura"+cod_factura+".pdf");
            DataOutputStream dout = new DataOutputStream(fout);
            dout.write(bt,0,bt.length);
            fout.close();
            System.out.println("aaaaaaaaaaaaaaaaaaaaaaaa");
            
        } catch (SQLException ex) {
            Logger.getLogger(FacturasPDF.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(FacturasPDF.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    

